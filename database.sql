-- MySQL schema for Student Performance Prediction System
-- Run: mysql -u root -p < database.sql

CREATE DATABASE IF NOT EXISTS student_performance;
USE student_performance;

CREATE TABLE IF NOT EXISTS admins (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(100) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS students (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_id VARCHAR(50) UNIQUE NOT NULL,
    name VARCHAR(150) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    age INT,
    gender VARCHAR(20),
    attendance FLOAT,
    internal_marks FLOAT,
    assignment_marks FLOAT,
    quiz_marks FLOAT,
    study_hours_per_day FLOAT,
    previous_semester_marks FLOAT,
    participation VARCHAR(20),
    internet_access VARCHAR(10),
    parent_education VARCHAR(50),
    final_exam_marks FLOAT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS predictions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    student_id VARCHAR(50) NOT NULL,
    predicted_marks FLOAT,
    pass_fail VARCHAR(10),
    grade VARCHAR(5),
    risk_level VARCHAR(20),
    suggestions TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (student_id) REFERENCES students(student_id) ON DELETE CASCADE
);

